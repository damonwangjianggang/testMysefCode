package com.ylch.ylcy.ylcy.shops.entitys;

public class DataBean {
	private String lid;
	private String bid;
	private String dic_type;
	private String dic_num;
	private String dic_name;

	public String getLid() {
		return lid;
	}

	public void setLid(String lid) {
		this.lid = lid;
	}

	public String getBid() {
		return bid;
	}

	public void setBid(String bid) {
		this.bid = bid;
	}

	public String getDic_type() {
		return dic_type;
	}

	public void setDic_type(String dic_type) {
		this.dic_type = dic_type;
	}

	public String getDic_num() {
		return dic_num;
	}

	public void setDic_num(String dic_num) {
		this.dic_num = dic_num;
	}

	public String getDic_name() {
		return dic_name;
	}

	public void setDic_name(String dic_name) {
		this.dic_name = dic_name;
	}

}