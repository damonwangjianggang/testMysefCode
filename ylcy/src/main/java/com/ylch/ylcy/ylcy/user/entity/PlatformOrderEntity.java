package com.ylch.ylcy.ylcy.user.entity;

public class PlatformOrderEntity {

	private String userid;
	private String orderId;
	private String orderAddr;
	private int comId;
	private String orderStatus;
	private String comName;
	private String comImgurl;
	
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	public String getComImgurl() {
		return comImgurl;
	}
	public void setComImgurl(String comImgurl) {
		this.comImgurl = comImgurl;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getOrderAddr() {
		return orderAddr;
	}
	public void setOrderAddr(String orderAddr) {
		this.orderAddr = orderAddr;
	}
	
	public int getComId() {
		return comId;
	}
	public void setComId(int comId) {
		this.comId = comId;
	}
	public String getOrderStatus() {
		return orderStatus;
	}
	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}
	@Override
	public String toString() {
		return "PlatformOrderEntity [userid=" + userid + ", orderId=" + orderId + ", orderAddr=" + orderAddr
				+ ", comId=" + comId + ", orderStatus=" + orderStatus + ", comName=" + comName + ", comImgurl="
				+ comImgurl + "]";
	}
	
	
}
